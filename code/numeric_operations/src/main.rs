fn main() {
    // addition
    let sum = 999_999 + 1; // 1 000 000
    println!("the sum is {}", sum);

    // subtraction
    let difference = 32.2 - 21.5; // 10.7
    println!("the difference is {}", difference);

    // multiplication
    let product = 3.14 * 2.; // 6.28
    println!("The product is {}", product);

    // division
    let quotient = 5./2.; // 2.5
    let floored = 5/2; // 2
    println!("division result: {}", quotient);
    println!("integer division result: {}", floored);

    // remainder 
    let remainder = 5 % 2;
    println!("the remainder is {}", remainder);
}
